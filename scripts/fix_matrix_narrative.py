def verify_narrative_fine(narrative: list[str], expected_fine: int) -> None:
    """Финальная строка 'Итого: N ₽.' обязана совпадать с expected.fine."""
    import re
    last = narrative[-1]
    m = re.search(r"(-?\d+)\s*₽", last)
    assert m, f"нет ₽ в финальной строке: {last!r}"
    assert int(m.group(1)) == expected_fine, (
        f"narrative Итого={m.group(1)} ≠ expected.fine={expected_fine}")


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Точечная правка narrative матрицы library_fine под новый эталон.")
    # dry-run ПО УМОЛЧАНИЮ (безопасный контракт, как у delete_reports.py):
    parser.add_argument(
        "--apply", action="store_true",
        help="записать изменения (по умолчанию — dry-run, только показать)")
    args = parser.parse_args()
    apply = args.apply

    matrix_path = Path(lg.MATRIX_PATH)
    data = json.loads(matrix_path.read_text(encoding="utf-8"))
    rows = {r["id"]: r for r in data["calculations"]["rows"]}

    for cid, narr in NARRATIVE.items():
        row = rows[cid]
        verify_narrative_fine(narr, row["expected"]["fine"])
        if row["calculation"] != narr:
            print(f"  m{cid:02d}: calculation переписан ({len(row['calculation'])} → {len(narr)} строк)")
            if apply:
                row["calculation"] = narr
    for cid, conds in CONDITIONS_FIX.items():
        row = rows[cid]
        if row["conditions"] != conds:
            print(f"  m{cid:02d}: conditions → {conds}")
            if apply:
                row["conditions"] = conds

    if apply:
        matrix_path.write_text(
            json.dumps(data, ensure_ascii=False, indent=2) + "\n",
            encoding="utf-8")
        print("ЗАПИСАНО")
    else:
        print("(dry-run)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
